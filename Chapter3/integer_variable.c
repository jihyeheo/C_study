#include <stdio.h>

int main(void)
{
	short sh = 12345;
	int in = 23;
	long ln = 333333333;
	long long lln = 21732485985;

	printf("short형 변수 출력 : %d\n", sh);
	printf("int형 변수 출력 : %d\n", in);
	printf("long형 변수 출력 : %ld\n", ln);
	printf("long long형 변수 출력 : %lld\n", lln);


	return 0;

}
