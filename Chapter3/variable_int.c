#include <stdio.h>

int main(void)
{
	int a;
	int b;
	int c;
	int d, e;
	double da;	//double형 변수 da 선언
	char ch;	// char형 변수 cc 선언

	a = 10;
	b = 5;
	c = a + 2;
	d = 33;
	e = d - 22;
	da = 3.5;
	ch = 'a';



	printf("변수 a 의 값 : %d\n", a);
	printf("변수 b의 값 : %d\n", b);
	printf("변수 da의 값 : %.1lf", da);
	printf("변수 cc의 값 : %c\n", ch);




}
